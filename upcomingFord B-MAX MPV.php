<h2> Ford B-MAX MPV Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The MPV segment is on a roll in   India and it seems that everyone wants a piece of the action. The latest   entrant is the Honda Mobilio which was launched in July and is   currently turning out to be one of the major successes for the Japanese   automaker. Renault will join the bandwagon with the Dacia designed Lodgy   while Ford is expected to enter the fray with the B-MAX MPV. It is   based on the Fiesta platform and shares much of its underpinnings and   design with the sedan.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd6.aeplcdn.com/0x0/ec/09/3C/14182/img/ol/Ford-B-MAX-MPV-Exterior-30438.jpg?t=150307613&amp;t=150307613" title="Ford B-MAX MPV" alt="Ford B-MAX MPV" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Ford B-MAX is underpinned by the Fiesta platform which means that   it shares much of its external design with the sedan. It gets the same   Aston Martin- esque -fascia right down to the shape of the headlights.   However, by virtue of this being an MPV, it gets a slightly more raked   bonnet which for obvious reasons is larger than its sedan counterpart.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd7.aeplcdn.com/0x0/ec/09/3C/14182/img/ol/Ford-B-MAX-MPV-Right-Side-30439.jpg?t=150344017&amp;t=150344017" title="Ford B-MAX MPV Right Side" alt="Ford B-MAX MPV Right Side" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis">The side profile once again resembles the Fiesta due to the flared   wheel arches and forward sloping roofline. The rear roofline ends   sharply at the D-pillar and then flows into the tail lamps. At the rear,   Ford has given the B-MAX wrap around tail lamps and has divided the   rear section into two parts through the use of prominent creases. The   first section runs from the spoiler to the middle of the boot lid where   the Ford logo is located while the second section runs from the number   plate holder to the loading lip.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The interior gets the same design as the Ford Fiesta sedan and   hatchback. This means a combination of grey and silver for most of the   dashboard while the upholstery will be a lighter shade of grey. Standard   features on the top- spec variant are expected to include 2-DIN music   system, climate control, SYNC technology, second row AC vents, one touch   seat folding for both second and third rows and finally a reversing   camera.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd8.aeplcdn.com/0x0/ec/09/3C/14182/img/ol/Ford-B-MAX-MPV-Interior-30440.jpg?t=150359470&amp;t=150359470" title="Ford B-MAX MPV Interior" alt="Ford B-MAX MPV Interior" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The B-MAX is likely to be introduced only with the 1.5-litre diesel   engine that produces 93bhp and 203Nm of torque. Power is sent to the   front wheels via a five-speed manual gearbox. If there is a petrol   version, it will be the 1.5-litre four pot motor that produces 109bhp   and 140Nm of torque. Europe also gets the 1.0-litre EcoBoost engined   version but this is unlikely to be launched for the Indian market.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd5.aeplcdn.com/0x0/ec/09/3C/14182/img/ol/Ford-B-MAX-MPV-left-rear-three-quarter-30441.jpg?t=150516790&amp;t=150516790" title="Ford B-MAX MPV left rear three quarter" alt="Ford B-MAX MPV left rear three quarter" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Price and competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The B-MAX is expected to be priced in the range of Rs 6 lakh to Rs 10   lakh and will compete with the Maruti Suzuki Ertiga, Chevrolet Enjoy   and the Honda Mobilio.</span></p>
