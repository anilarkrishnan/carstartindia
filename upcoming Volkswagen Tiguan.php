<h2> Volkswagen Tiguan Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Volkswagen India confirmed that   it will showcase three all-new products at the upcoming Auto Expo 2016.   One of these products will be the all-new 2016 VW Tiguan. The GTE   concept might be shown at the event, however, we can expect the Tiguan's   production version to be launched in India some time in 2016. The new   Tiguan debuted at the Frankfurt Motor Show in September 2015. It is   based on the MQB platform, making it lighter than the outgoing model.   The new Tiguan boasts of an all new design, better equipment, and a   refined engine lineup. Its introduction in the Indian market will mean   VW's entry into the compact SUV segment in India.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Tiguan's design is influenced by the VW Cross Coupe GTE showcased   at the 2015 Detroit Auto Show. It measures 4,485mm in length, 1,839mm   in width, 1,633mm in height and has a wheelbase of 2,682 mm. As compared   to the outgoing model, this new vehicle is larger than in size (70mm   longer, 30.5mm wider and 76mm longer in wheelbase). Up front are a pair   of angular LED headlamps with LED DRLs directly flowing into the grille   and chrome elements on the front wing.</span></p>
  <p><span id="spnLargeDescSynopsis">The car rides on large 18-inch diamond cut alloy rims and gets a   chrome line that runs across the side. There is also a chrome   underlining for the window line and roof rails. At the back, a sleek   pair of new horizontal LED tail lamps are visible and a new roof spoiler   adds to the appeal. A chrome appliqué on the lower fascia and twin   chrome exhaust tips complete the rear look.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The upcoming VW Tiguan's cabin is similar to the previous car's   layout but gets a premium design with new leather and other materials.   VW globally offers an optional 12.3-inch Active Info display from the   2015 VW Passat, which displays the instrument dials and navigation in   full screen. A five-inch non-colour infotainment display is standard   while an eight-inch infotainment touchscreen is available as optional   equipment. The latter two might be available in the India-spec variant.</span></p>
  <p><span id="spnLargeDescSynopsis">The SUV also features a new three-zone automatic climate control   system called Pure Air Climatronic. The other highlights in the Tiguan   include a large panoramic sunroof and electric four-way lumbar support   and massage function for the front seats. On the safety front, even in   the base variant, VW is expected to offer safety features like the   Automatic Post-Collision Braking System, City Emergency Braking and   Pedestrian Monitoring and Lane Assist departure warning system. There   are in all, seven airbags, including a knee airbag on the driver’s side.   The top-of-the-line versions will be equipped with adaptive cruise   control (ACC), Side Assist (lane-changing assistant), Pre-Crash   proactive occupant protection and Area View (360-degree view).</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">There is no official word from VW about the powertrain yet. However,   we can expect the Tiguan to be powered by a choice of 1.4- and 2.0-litre   TSI petrol engine options and on the diesel front, the 1.6- and   2.0-litre TDI diesel units. The transmission options might include the   seven-speed DSG unit or maybe even the new 10-speed DSG gearbox.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Price and competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The VW Tiguan is expected to be available in Trendline, Comfortline   and Highline trims. It will go up against the crossovers like the Audi   Q3 and BMW X1 in the Rs 28 lakh to Rs 38 lakh price segment in our   country.</span></p>
