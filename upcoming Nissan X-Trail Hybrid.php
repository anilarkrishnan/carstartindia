<h2> Nissan X-Trail Hybrid Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Earlier shown at the 2016 Auto   Expo, the Nissan X-Trail Hybrid is on its way to the showrooms here in   India. While Nissan India officially sold the first and the second   generation of the X-Trail, the brand never had the current-gen model   (introduced in 2013) up until now. The third-gen model, in fact, will be   introduced in the eco-friendly hybrid form.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">On the outside, the X-Trail Hybrid looks quirky and striking thanks   to the high-set hood and the ‘V’ chrome accent on the grille. Also   improving the road presence are the striking pair of headlights   featuring LEDs. In profile, the X-Trail remains calm and features subtle   design curves. The delicately flared wheel arches and minimal cladding   all around lend a very functional look to this car. Lastly, the rear-end   design, though not as impactful as the front-end, looks attractive with   those nicely detailed taillights and a big tailgate.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Interior wise, the dashboard is angled towards the driver and has a   distinct ridge flowing across the top half that moves on to the door   pads. The unique looking three-spoke steering wheel gets a silver accent   which matches those on the centre console and the door pads.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The India-bound X-Trail is all about its clever drivetrain. There’s a   2-litre, in-line 4-cylinder petrol engine paired to a 31kW electric   motor for a total power output of around 180bhp. All that power is sent   to the road via Nissan's Dual Clutch Control. This hybrid arrangement is   available in Nissan’s front-engine, front-wheel-drive cars only which   means the forthcoming X-Trail Hybrid will not feature all-wheel drive.   The X-Trail Hybrid will be introduced as a fully imported model which is   why we are expecting it to cost well over Rs 30 lakh. Now even though   that is expensive, buyers would be pleased to know there is nothing   quite like the X-trail Hybrid as it’s the first ever luxury hybrid   crossover to be sold in India.</span></p>
