        <td width="558" height="337"><p align="center" class="style21">CARS...?</p>
          <p>A <strong>car</strong> is a wheeled, self-powered <a href="https://en.wikipedia.org/wiki/Motor_vehicle" title="Motor vehicle">motor vehicle</a> used for <a href="https://en.wikipedia.org/wiki/Transportation" title="Transportation">transportation</a> and a product of the <a href="https://en.wikipedia.org/wiki/Automotive_industry" title="Automotive industry">automotive industry</a>.   Most definitions of the term specify that cars are designed to run   primarily on roads, to have seating for one to eight people, to   typically have four wheels with <a href="https://en.wikipedia.org/wiki/Tyre_%28wheel%29" title="Tyre (wheel)">tyres</a>, and to be constructed principally for the transport of people rather than goods.The year 1886 is regarded as the birth year of the modern car. In that year, German inventor <a href="https://en.wikipedia.org/wiki/Karl_Benz" title="Karl Benz">Karl Benz</a> built the <a href="https://en.wikipedia.org/wiki/Benz_Patent-Motorwagen" title="Benz Patent-Motorwagen">Benz Patent-Motorwagen</a>.   Cars did not become widely available until the early 20th century. One   of the first cars that was accessible to the masses was the 1908 <a href="https://en.wikipedia.org/wiki/Ford_Model_T" title="Ford Model T">Model T</a>, an American car manufactured by the <a href="https://en.wikipedia.org/wiki/Ford_Motor_Company" title="Ford Motor Company">Ford Motor Company</a>. Cars were rapidly adopted in the United States of America, where they replaced <a href="https://en.wikipedia.org/wiki/Draft_animal" title="Draft animal">animal-drawn</a> <a href="https://en.wikipedia.org/wiki/Carriage" title="Carriage">carriages</a> and carts, but took much longer to be accepted in Western Europe and other parts of the world.</p>
          <p>Cars are equipped with controls used for driving, parking, passenger   comfort and safety, and controlling a variety of lights. Over the   decades, additional features and controls have been added to vehicles,   making them progressively more complex. Examples include rear reversing   cameras, <a href="https://en.wikipedia.org/wiki/Automobile_air_conditioning" title="Automobile air conditioning">air conditioning</a>, <a href="https://en.wikipedia.org/wiki/Automotive_navigation_system" title="Automotive navigation system">navigation systems</a>, and <a href="https://en.wikipedia.org/wiki/In_car_entertainment" title="In car entertainment">in car entertainment</a>. Most cars in use in the 2010s are propelled by an <a href="https://en.wikipedia.org/wiki/Internal_combustion_engine" title="Internal combustion engine">internal combustion engine</a>, fueled by <a href="https://en.wikipedia.org/wiki/Deflagration" title="Deflagration">deflagration</a> of <a href="https://en.wikipedia.org/wiki/Gasoline" title="Gasoline">gasoline</a> (also known as petrol) or <a href="https://en.wikipedia.org/wiki/Diesel_fuel" title="Diesel fuel">diesel</a>. Both fuels cause <a href="https://en.wikipedia.org/wiki/Air_pollution" title="Air pollution">air pollution</a> and are also blamed for contributing to <a href="https://en.wikipedia.org/wiki/Climate_change" title="Climate change">climate change</a> and <a href="https://en.wikipedia.org/wiki/Global_warming" title="Global warming">global warming</a>. Vehicles using <a href="https://en.wikipedia.org/wiki/Alternative_fuel" title="Alternative fuel">alternative fuels</a> such as <a href="https://en.wikipedia.org/wiki/Ethanol_fuel" title="Ethanol fuel">ethanol</a> <a href="https://en.wikipedia.org/wiki/Flexible-fuel_vehicle" title="Flexible-fuel vehicle">flexible-fuel vehicles</a> and <a href="https://en.wikipedia.org/wiki/Natural_gas_vehicle" title="Natural gas vehicle">natural gas vehicles</a> are also gaining popularity in some countries. <a href="https://en.wikipedia.org/wiki/Electric_car" title="Electric car">Electric cars</a>, which were invented early in the history of the car, began to become commercially available in 2008.</p>
          <p>There are costs and benefits to car use. The costs of car usage   include the cost of: acquiring the vehicle, interest payments (if the   car is financed), repairs and <a href="https://en.wikipedia.org/wiki/Auto_maintenance" title="Auto maintenance">auto maintenance</a>, fuel, <a href="https://en.wikipedia.org/wiki/Depreciation" title="Depreciation">depreciation</a>, driving time, <a href="https://en.wikipedia.org/wiki/Parking_fee" title="Parking fee">parking fees</a>, taxes, and insurance.The costs to society of car use include: <a href="https://en.wikipedia.org/wiki/Maintaining_road" title="Maintaining road">maintaining roads</a>, <a href="https://en.wikipedia.org/wiki/Land_use" title="Land use">land use</a>, <a href="https://en.wikipedia.org/wiki/Road_congestion" title="Road congestion">road congestion</a>, air pollution, <a href="https://en.wikipedia.org/wiki/Public_health" title="Public health">public health</a>,   health care, and disposing of the vehicle at the end of its life. Road   traffic accidents are the largest cause of injury-related deaths   worldwide.</p>
          <p>The benefits may include on-demand transportation, mobility, independence, and convenience.The societal benefits may include: economic benefits, such as job and   wealth creation from car production, sales and maintenance,   transportation provision, society well-being derived from leisure and   travel opportunities, and revenue generation from the <a href="https://en.wikipedia.org/wiki/Category:Vehicle_taxes" title="Category:Vehicle taxes">tax</a> opportunities. The ability for humans to move flexibly from place to   place has far-reaching implications for the nature of societies .It was estimated in 2010 that the number of cars had risen to over 1 billion vehicles, up from the 500 million of 1986.The numbers are increasing rapidly, especially in <a href="https://en.wikipedia.org/wiki/Automotive_industry_in_China" title="Automotive industry in China">China</a>, <a href="https://en.wikipedia.org/wiki/Automotive_industry_in_India" title="Automotive industry in India">India</a> and other <a href="https://en.wikipedia.org/wiki/Newly_industrialized_country" title="Newly industrialized country">newly industrialized countries</a>.</p>
