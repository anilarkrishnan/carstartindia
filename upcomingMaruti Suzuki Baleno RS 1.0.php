<h2> Maruti Suzuki Baleno RS 1.0 Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Maruti Suzuki will soon make the   Baleno a lot feistier by introducing the hatchback with a turbocharged   petrol engine. Unveiled at the 2015 Geneva Motor Show, the 1-litre   three-cylinder turbocharged engine christened ‘BoosterJet’ will make its   India debut at the 2016 Auto Expo.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Baleno with the 1-litre BoosterJet carries the same look as the   regular model which means it uses Suzuki’s new liquid flow design   philosophy to emphaize its exterior. The front of the car gets a   V-shaped grille and sweptback headlamps that reminds us of the Swift.   Although the side profile is relatively simple, the rear-end looks   pretty bold with LED tail lamps and some splashing of chrome. That being   said, this 1-litre turbocharged version could get bigger alloys and   some detailing to differentiate it from its naturally aspirated   counterpart.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Like the exterior, the cabin design and the overall layout will   remain the same. There will however be slightly revised instrumentations   and perhaps different upholstery for a sportier look. If we look at the   existing Baleno range, there is plenty of space in the cabin for five   passengers, also the boot is large at 339litres. Maruti is offering   plenty of features with the Baleno with the top-end model featuring a   touch-screen music system with navigation, rear parking camera and   sensors, climate control and engine start-stop button.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and Performance</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Currently, petrol versions of the Baleno are powered by the   1.2-litre, K-Series VVT engine developing 83bhp and 115Nm of torque. The   fuel efficiency of this engine is officially rated at 21.4kmpl while   Maruti is offering it with a choice of a 5-speed manual or a CVT   automatic gearbox. The Baleno 1-litre BoosterJet, on the other hand,   uses forced induction to dish out 110bhp and 170Nm of torque – numbers   that should allow for a noticeable improvement in outright performance.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Baleno range competes with the likes of Honda Jazz and Hyundai   Elite i20 here in India. The Baleno 1-litre BoosterJet, meanwhile, will   compete with the Volkswagen Polo GT TSI as its arch rival.</span></p>
