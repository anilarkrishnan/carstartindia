<h2>Hyundai HND Preview </h2>
<div> <span id="spnLargeDescSynopsis">
  <p>India is a hot market for SUVs of   all sizes and the compact SUV market is the fastest growing segment. It   took Maruti a few years to warm up to it after the launch of the Ford   EcoSport. Not wanting to be left behind, Hyundai unveiled the HND-14   concept of a new compact SUV at the Auto Expo 2016 held in Greater   Noida.</p>
  <p>The HND-14 concept on display was more of a design study but had all   the elements to make an impressive sub-four metre SUV. The high bonnet   and the square lines give it the SUV look. The slightly flared wheel   arches housing those large chunky wheels looked in place. While the   production car will not be as loud, it certainly will take a lot of cues   from the handsome concept.</p>
  <p>The concept does not reveal anything inside the car or under the   hood. But an informed guess would mean that the production car will get a   host of features, a futuristic cabin and largely efficient powertrain   options. The production version may come out within a year or two and we   would certainly be looking out to point out the similarities between   this concept and the real car. </p>
