<h2> Audi Q2 Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Audi has unveiled their smallest   SUV yet at the Geneva Motor Show. Built on Volkswagen’s MQB platform,   the Q2 will sit under the Q3 in the German manufacturer’s SUV portfolio.   The compact SUV has been built with an intent to make luxury SUVs more   approachable for buyers.</span></p>
  <p><span id="spnLargeDescSynopsis">The Q2 is a compact SUV and at 4191mm, it sits between the Maruti   Suzuki Grand Vitara and the Renault Duster in terms of length and for   that matter, the overall size. In spite of its puny dimensions, the Q2   looks its part as an SUV with the big not-exactly-hexagonal front grille   and a chunky air dam sitting below it. The projector headlamps have   integrated LED DRLs. The bash plate below the air dam, the skirt   cladding around the car and another bash-plate on the rear bumper add   brawn to the SUV. The side profile is estate-like with the large wheels   and flared wheel arches giving it the SUV feel.</span></p>
  <p><span id="spnLargeDescSynopsis">Well, it is an Audi and the interior will be luxurious. Being an   entry-level car, the equipment list will be slightly curtailed, but   dual-zone climate control, cruise control, power seats, electrically   operated wing mirrors and an optional sunroof will be available. The   interior is dressed in black and gets body coloured highlights. The   infotainment system gets a display screen that sits on the dash, above   the round AC vents. The twin-pod instrument cluster has an MID that   reads out the vitals.</span></p>
  <p><span id="spnLargeDescSynopsis">The Audi Q2 is offered with a variety of engine options globally. But   in India, you may have the popular 2.0-litre TDI with the 148bhp setup   with an optional Quattro all-wheel-drive. There is also a chance of the   1.6-litre four cylinder oil-burner making a debut in Audi cars in India.   As for the petrol options, you may get the 1.4-litre TSI from the Jetta   working under the Audi hood. All the engines will be mated to the   seven-speed S-Tronic dual clutch gearbox.</span></p>
  <p><span id="spnLargeDescSynopsis">The Audi Q2 targets the Mercedes GLA and the BMW X1 globally and in   India, we will have it competing with the Mini Countryman as well as the   Volvo V40 Cross Country. The Q2 will go on sale globally in November   and will take at least one more year to hit the Indian shores.</span></p>
</div>
