<h2> Volkswagen Passat GTE Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Volkswagen has unveiled the next   generation Passat at the Auto Expo 2016 and is expected to replace the   existing car soon. Built on the versatile MQB platform, the new Passat   was showcased with a plug-in hybrid powertrain.</span></p>
  <p><span id="spnLargeDescSynopsis">The new Passat gets the Volkswagen family face with a wide   horizontally slatted front grille which fuses into the swept back   headlamps. Daytime running lamps have been integrated into the   intelligent LED headlamps. While the sleek straight lines remain the   same, the new tail lamps look premium. The blue detailing highlights the   hybrid powertrain.</span></p>
  <p><span id="spnLargeDescSynopsis">The interiors are premium and despite the number of buttons, the   dashboard does not look too busy. The analog chronograph sits at the   centre of the dashboard that hides the AC vents between the horizontal   slats. The instrument cluster is a TFT screen with digital analog dials   reading out the speed and the engine revs along with other information.   Two electronic trip meters, average speed, average fuel economy,   instantaneous fuel economy, gear indicator with shift assist will be   standard.</span></p>
  <p><span id="spnLargeDescSynopsis">It is a premium D-segment sedan and hence features like dual-zone   climate control, cruise control, touch-screen infotainment system, power   adjustable front seats will be standard. The music system can be   accessed with steering mounted controls and it has Bluetooth, USB and   AUX connectivity options. It may retain the automatic parking feature   from the outgoing model but will have parking sensors standard across   the range. The Passat, like all premium sedans, will offer a safety net   of airbags, ABS with EBD, ESP, hill assist et al.</span></p>
  <p><span id="spnLargeDescSynopsis">The highlight of the Passat GTE is the plug-in hybrid powertrain that   combines a 1.4-litre turbocharged petrol engine with an 113bhp electric   motor. The 1.4-litre TSi mill is the same as seen in the current Skoda   Octavia and the Volkswagen Jetta and develops 152bhp of power. Together,   the power output is rated at 214bhp of power and 400Nm of torque. The   front wheels are powered through the seven-speed DSG unit. If you want   to go all electric, your driving range is limited to 50kms beyond which   you will either have to switch to petrol or find a power source to   charge those batteries.</span></p>
  <p><span id="spnLargeDescSynopsis">While the infrastructure in India is not very conducive for plug-in   hybrids, the success of the similarly priced Toyota Camry Hybrid and the   news of the upcoming Honda Accord Hybrid has had a positive effect. The   Passat GTE may also get exemptions under the FAME initiative by the   Government of India which supports environment-friendly products in the   country.</span></p>
