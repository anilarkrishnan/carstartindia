<h2> Volkswagen Polo GTi Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">If a Volkswagen gets the GTi   badge, it has to be a hatchback and it has to be fast. The GTi badge has   been associated with hot hatches since decades and finally, it will   come to India, officially. The first GTi to be brought here is the Polo   GTi, a souped up 3-door version of the Volkswagen Polo.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd2.aeplcdn.com/600x337/cw/ec/21687/Volkswagen-Polo-GTi-Rear-view-64995.jpg?wm=0" title="Volkswagen Polo GTi" alt="Volkswagen Polo GTi" height="337" width="599" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Exteriors</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Well, it is a Polo. While the headlamps remain the same (with a   possibility of DRLs being integrated) while the grille and the front   bumper are new. It gets a mesh grille with the GTi badge perched on the   left corner. The bumper is sharper and also gets a wide mesh grille with   integrated fog lamps. While the side profile remains same, the rear   doors have been ditched. The tail is the same except for the twin pod   tail pipe that stands out.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The GTi will be based on the Highline variant of the Polo. It gets   height adjustable driver seat, electrically adjustable and folding   mirrors and cruise control. The 2-DIN music system has USB, AUX and   Bluetooth compatibility. The interiors are black with a flat bottom   sporty steering wheel with steering mounted audio controls.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd2.aeplcdn.com/600x337/cw/ec/21687/Volkswagen-Polo-GTi-Interior-64996.jpg?wm=0" title="Volkswagen Polo GTi" alt="Volkswagen Polo GTi" height="337" width="599" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and Performance</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Well, this is the most exciting part for the enthusiasts. It gets a   1.8-litre turbocharged petrol engine from the Octavia churning out at   least 180bhp of power. While it gets the expected DSG automatic gearbox,   enthusiasts will certainly be grinning as a manual transmission option   will also be offered. The Polo GTi hits the ton from standstill in under   seven seconds.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd4.aeplcdn.com/600x337/cw/ec/21687/Volkswagen-Polo-GTi-Engine-Bay-64997.jpg?wm=0" title="Volkswagen Polo GTi" alt="Volkswagen Polo GTi" height="337" width="599" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Price and Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Polo GTi will dethrone the Abarth Punto to be the most powerful   hatchback except for the Mini Cooper S and is expected to be priced over   Rs 20 lakh. In that case, the BMW 1 Series, Audi A3 and the Mercedes   A-Class are also within the reach for someone who is not performance   focussed. </span></p>
