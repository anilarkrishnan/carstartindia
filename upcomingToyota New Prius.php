<h2> Toyota New Prius Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The Toyota Prius needs no   introduction. One of the highest selling hybrid cars in the world, the   Prius has been endorsed by everyone from Greenpeace and Hollywood   celebrities. Toyota has been selling the Prius in India for quite some   time now and has unveiled the next generation Prius at the Auto Expo   2016.</span></p>
  <p><span id="spnLargeDescSynopsis">The fourth-generation Toyota Prius gets all new looks. While we could   best describe the looks as alien, the car looks striking. The   boomerang-like headlamps, vertical LED fog lamps and a snout-like front   bumper can best be called futuristic by all standards. Moving on to the   side, it is a four-door coupe with a sloping roofline that blends into   the notchback. The cabin seems spacious for four with enough headroom in   the back. The rear also gets the boomerang-like lamps with LEDs flowing   down from the top of the boot. The creases and cuts toe the   alien-design line from the front. As for the dimensions, the new Prius   is 60mm longer, 15mm wider and 20mm lower than the old Prius.</span></p>
  <p><span id="spnLargeDescSynopsis">The dashboard is a geek paradise with its many buttons, screens and   warning lights. The speedometer is digital and the infotainment system   is touchscreen. The infotainment system can be connected to your phone   via Bluetooth and gets the latest software like Apple Car Play, Android   Auto and MirrorLink. Dual-zone automatic climate control, automatic   headlamps, cruise control and automatic climate control are standard. It   gets airbags, an electronic stability programme with emergency braking.</span></p>
  <p><span id="spnLargeDescSynopsis">The Prius is a hybrid and has a 1.8-litre four cylinder petrol engine   paired with electric motors to make up its powertrain. While the setup   is same as before, the engine has been tuned for better efficiency. The   batteries have a higher energy density and the motors are smaller. The   efficiency has improved by almost 10 per cent and Toyota expects the   Prius to deliver 40kmpl.</span></p>
  <p><span id="spnLargeDescSynopsis">The current Prius is offered at about Rs 38 lakh in India. The new   generation Prius will also be imported as a CBU with an estimated price a   tad under Rs 40 lakh ex-showroom Delhi.</span></p>
