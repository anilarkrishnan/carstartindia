<h2> Audi SQ7 Preview </h2>
<div>
  <p>The Audi Q7 has been largely   popular in the Indian market and with the launch of the new generation   Q7 last year, the flagship Audi SUV has managed to stay in the front of   the race for large luxury SUVs. But the Ingolstadt-based manufacturer   has always missed one thing in the Q7 â€“ a powerhouse performance car.   Yes, they had the 500bhp 6.0-litre V12 oil-burner, but it never really   appealed like the ML 63 AMG or the BMW X5M. Audi now has built the SQ7   to take on the performance-packed luxury SUVs and is expected to come to   India pretty soon.</p>
  <p>Let us get to the meat first. Under the hood you have a 4.0-litre V8   twin-turbocharged diesel engine. Unlike its petrol competitors, Audi has   demonstrated its prowess of building performance diesel engines. And it   is the first production car to have an electrically driven   turbocharger. Developing 435bhp of power and 900Nm of torque the Audi Q7   powers past the 100kmph mark from rest in 4.8 seconds. It is equipped   with an eight-speed automatic transmission coupled up with the famed   Quattro all-wheel-drive.</p>
  <p>As for the aesthetics, you have the front grille with the S-badge and   an aggressive front bumper with horizontal slats covering the air   intakes and Matrix LED headlamps. The wing mirrors will be painted in   contrast and it gets 20-inch alloys as standard. The two twin exhausts   in the rear will give out the sleeper for the trained eyes. Inside, the   SQ7 will continue to be luxurious. The upholstery will be done in fancy   leather and all creature comforts will be on offer if not standard   already.</p>
  <p>The SQ7, as we have said before, will like to take on the BMW X5 M or   the Mercedes GLE63 AMG or the GLS 63AMG and the top-spec Land Rover   Discovery in terms of its price and appeal. The SQ7 will certainly tip   the scale onward of a crore rupees if not by much. </p>
