<h2> Nissan GT-R Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Considered by many to be a   Porsche 911 killer at the time of its launch, the Nissan GT-R has   evolved to become one of the fastest and yet highly practical supercars   of all time. What’s even more impressive is the fact that developments   on this all-wheel drive coupe has never really slowed down, with Nissan   introducing yearly updates with more power and upgraded mechanicals.   Now, Nissan is finally planning to bring the GT-R to India by the end of   this year to take on the mighty Porsche 911 Turbo.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The GT-R is all about sheer speed. As a result, from the overall   design to the smallest details, Nissan has focused on keeping the design   as aerodynamic as possible. While the uninspiring look of the front-end   isn’t the most impressive piece of design in the supercar world, the   GT-R makes up for it with its jet fighter-style canopy, striking multi   spoke alloys and that massive rear wing. Lastly, the signature Skyline   look at the rear is courtesy the traditional four-ring taillights, while   air flow below the vehicle is handled by the carbon fibre underbody   diffuser.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Given its legacy, it comes as no surprise that the interior of the   R35 generation GT-R is a tech-fest full of computers and high-tech   sensors monitoring even the tiniest of movements of the car. The   dashboard is dominated by a massive screen that displays all the   functions of the car.   The GT-R may be termed as a four-seater but it essentially features a   2+2 configuration, meaning, the rear seats are best reserved only for   short journeys.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The GT-R is powered by a 3.8-litre V6 engine. Now it may not sound   like a lot in supercar space, however, Nissan has backed this highly   advanced engine with two turbochargers. The end result is 542bhp of   power and 612Nm of torque. All of that grunt is delivered on to the road   via a 6-speed automatic gearbox and what’s probably the most effective   all-wheel drive system ever. The super advanced all-wheel drive system   in the GT-R allows this 1.8-tonne vehicle to accelerate from 0-100kmph   in well under 3 seconds and gives it immense grip around the corners.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Like in the global markets, Nissan India will position the GT-R close   to the Porsche 911 range. The GT-R, in fact, will compete against the   911 Turbo as the latter is also a high-performance all-wheel drive coupe   with decent levels of practicality for a supercar.</span></p>
