<h2> Tata Pelican Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Tata's manufacturing facility at   Sanand will be heavily utilised soon and the company's upcoming new   products are the reason for it. While Tata Motors is busy developing a   hatchback and another compact sedan under the project name 'Kite', the   company is developing a new small car dubbed as the 'Pelican' as well.   This new small hatchback will be the upcoming rival of the Maruti Suzuki   Alto and is expected to go on sale in 2016-2017.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Tata Pelican is still under development and so there are no   particular details available. However, the new vehicle is expected to be   based on Tata Nano's platform. This re-engineered platform will   underpin this lightweight car that will also be exported to Tata's few   markets across the globe when its production starts. In terms of   appearance, the tall-boy design yet very compact body structure will be   retained. The car will get completely new headlamps, tail lamps and   bumpers that will differentiate it from the Nano on sale. The company   might also take inputs from their design studios in Pune (India),   Coventry (UK), and Turin (Italy) in the development of the car.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Reports till now haven’t confirmed what all features might be   introduced in the cabin of the Pelican. Nevertheless, with the   introduction of new products like the Zest and Bolt, we think the   company will bring in many new features that are in line with Tata's   HorizonNext strategy. Since the carmaker has already collaborated with   MapMyIndia for the use of GPS navigation technology in the Bolt, we can   expect this feature to be available in the top-end version of the new   Pelican as well.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and Gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Tata Pelican will be powered by 1.0-litre petrol and 800cc diesel   motor that Tata Motors is developing in-house. It is expected to be the   most affordable diesel car in India then. The transmission options will   continue to be the five-speed manual gearbox. The possibility of the   AMT gearbox can’t be ruled out, though it would increase the price of   the car.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Price and Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Tata Pelican will primarily rival the Maruti Suzuki 800 and the   Hyundai Eon; and the also compete with the upcoming Renault XBA and   Datsun Redi-GO. The new small car will be positioned above the Tata Nano   and is expected to be priced from Rs 2.5 lakh onwards.</span></p>
</div>
