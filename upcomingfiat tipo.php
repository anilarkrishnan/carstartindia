<h2> Fiat Tipo Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Fiat will replace the ageing   Linea sedan next year. Its successor is likely to be this new machine   called the Egea, a premium C-segment sedan which was shown as the Aegea   concept car earlier this year and has now been christened the Egea.   Globally it replaces the Bravo and has already been put into production   at Fiat’s plant in Turkey for a majority of LHD markets.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <span id="spnLargeDescSynopsis">Fiats have always been known to make good looking cars and this one, the   Egea seems to be no less. The front is dominated by a new ‘in your face   chrome grille’ which has the familiar Fiat logo right in the middle. </span>
  <p><span id="spnLargeDescSynopsis">The bonnet gets some muscular bulges while the headlamps are shaped   like thin battle axes and add quite a bit to the face of the car. Move   over to the side and you can see the familiar Fiat sedan lines thanks to   the strong shoulder line and softly flared wheel arches. At the rear we   can see the wrap around tail lamps and chrome strip above the number   plate slot.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The cabin seems to be more of an updated affair rather than a   completely new design. It includes elements like a completely new   instrument cluster, infotainment system and climate control with   physical knobs. Given its size and length, we expect that it will be   quite spacious at the rear and will be sold with accessories like rear   AC vents, 12V socket, sun shades and arm rest with storage space. There   is also a possibility that a screen may be included as an optional   extra.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">On the engine front, the Egea will be offered with four engines- two   petrol and two diesels. The former is expected to include the 1.4-litre   Twinair and most likely the 1.4-litre TJet petrol for markets like   India. On the diesel front, the engines are expected to be the 1.3-litre   Multijet as well as a 1.6-litre Multijet. Transmission options are   expected to include a six-speed MT as well as a six-speed AT.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Expect an India launch for the Egea during this time next year though   we suspect that it will have a different name. It will be their   C-segment competitor and will replace the current Linea which is then   expected to replace the Linea Classic as has been the case with the   current cars. It will be priced in the current car’s bracket and compete   with vehicles like the Honda City, Nissan Sunny, Maruti Suzuki Ciaz,   Hyundai Verna and the Renault Scala.</span></p>
