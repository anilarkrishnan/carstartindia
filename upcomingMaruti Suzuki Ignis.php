<h2> Maruti Suzuki Ignis Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The Ignis, which comes from the   parent car manufacturer Suzuki, will make its India debut at the Auto   Expo. The car was first showcased as the Suzuki iM-4 concept at the 2015   Geneva Motor Show and later unveiled at the 2015 Tokyo Motor Show.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd2.aeplcdn.com/600x337/cw/ec/21716/Maruti-Suzuki-Ignis-Exterior-65126.jpg?wm=0" alt="Maruti Suzuki Ignis" width="625" height="347" title="Maruti Suzuki Ignis" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The minimalist body consists of plain surfaces and round roof with   straight lines that give it a distinctive stance. The proportionate   tyres jutting out at the four corners firmly support the body and hint   at the car's muscular look. Dramatic features such as the front fascia   with its sunglass-clad look, the shell-like roof, and the attractive   slits on the C-pillar accentuate its exterior. The Maruti Ignis will   measure 3,700mm in length, 1,660mm in width, 1,595mm in height, 2435mm   in wheelbase and feature 180mm of ground clearance. The car will also   get LED daytime running lights.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd3.aeplcdn.com/600x337/cw/ec/21716/Maruti-Suzuki-Ignis-Rear-view-65127.jpg?wm=0" title="Maruti Suzuki Ignis" alt="Maruti Suzuki Ignis" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Ignis will get typical Maruti Suzuki black interiors with silver   accents. The interior quality will be very similar to that of the Maruti   Suzuki Swift in India. The car will get automatic climate control, a   modern touchscreen SmartPlay infotainment system equipped with USB and   BlueTooth connectivity, reverse parking sensors and a multifunction   steering wheel. The Ignis will be very similar to the Swift in terms of   equipment.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd1.aeplcdn.com/600x337/cw/ec/21716/Maruti-Suzuki-Ignis-Interior-65128.jpg?wm=0" title="Maruti Suzuki Ignis" alt="Maruti Suzuki Ignis" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The India-spec Ignis will receive the 1.2-litre K-Series petrol   engine and the 1.3-litre DDiS diesel engine with a 5-speed manual   gearbox. The petrol engine makes 83bhp and 115Nm of torque while the   diesel makes 74bhp and 190Nm of torque in the Swift. The figures may   vary in the Ignis as it could get a different state of tune. It may get   an optional CVT gearbox.</span></p>
