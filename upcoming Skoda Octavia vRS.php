<h2> Skoda Octavia vRS Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Skoda recently launched the   Octavia sedan for the Indian market and it has been doing pretty good   numbers for the German-owned Czech automaker thanks to its extensive   feature list and engine options. However, in the tradition of the   Octavia/Laura for the Indian market, Skoda is expected to bring to the   Indian market (next year) a high performance version of the D-Segment   sedan called the Octavia vRS. The car was unveiled at the 2013 Goodwood   Festival of Speed and finally launched at the 2013 Frankfurt Motor Show   in September.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd5.aeplcdn.com/0x0/ec/61/2B/11723/img/l/Skoda-Octavia-vRS-Right-Front-Three-Quater-22453.jpg?t=110141377&amp;t=110141377" title="Skoda Octavia vRS" alt="Skoda Octavia vRS" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Skoda Octavia vRS features the same overall design as the   standard car but with much sportier overtones. It gets black headlights,   sporty bumper with a mesh grille and a different design for the fog   lamps. The Skoda moustache grille seems to flow into the front of the   car and adds to the overall sporty appearance. On the sides, the Octavia   vRS has a similar profile to the standard car but gets sportier alloy   wheels and consequently bigger tyres.  The rear profile includes dual   quad-shaped exhausts and a small rear wing that has been fitted to the   boot door.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd6.aeplcdn.com/0x0/ec/61/2B/11723/img/l/Skoda-Octavia-vRS-left-rear-three-quarter-22454.jpg?t=110202050&amp;t=110202050" title="Skoda Octavia vRS left rear three quarter" alt="Skoda Octavia vRS left rear three quarter" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Interiors</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The vRS gets all the features of the standard car in addition to   sports-style bucket seats, a three-spoke steering wheel and ambient   lighting. Dual-zone climate control and a touchscreen infotainment   system also come as standard in the vRS. We also expect features like   paddle shifters, traction control and ESP to be standard on this car.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd7.aeplcdn.com/0x0/ec/61/2B/11723/img/l/Skoda-Octavia-vRS-Interior-22455.jpg?t=110227633&amp;t=110227633" title="Skoda Octavia vRS Interior" alt="Skoda Octavia vRS Interior" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Engine options</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The vRS version for Europe is powered by two engine options; petrol   and a diesel.  The petrol motor is a 2.0-litre turbocharged   four-cylinder engine producing 220bhp power hitting 100kph in 6.8   seconds. The diesel engine is also a turbocharged 2.0-litre unit that   develops 184bhp power and 380Nm torque aiding a 100kmph drag time in 8.1   seconds. The UK spec Octavia is also offered with the 1.2-litre TSI and   seven-speed DSG box found in the Polo. It is likely that for the first   time that such an engine option may be offered here too. Both these   engines are sourced from VW, who use it to power the performance   variants of the Jetta and the Golf. We also expect that Skoda has tuned   the suspension and directed the handling to focus on the sportier side</span></p>
  <span id="spnLargeDescSynopsis">. <img src="http://imgd8.aeplcdn.com/0x0/ec/61/2B/11723/img/l/Skoda-Octavia-vRS-Gear-Lever-22456.jpg?t=110317850&amp;t=110317850" title="Skoda Octavia vRS Gear Lever" alt="Skoda Octavia vRS Gear Lever" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Price and when can we expect the car in India</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Skoda Octavia vRS is likely to be priced in the range of Rs 18   lakh to Rs 21 lakh and will be unveiled at the 2014 Indian Auto Expo. We   can expect a launch in India a few months after the unveiling.</span></p>
