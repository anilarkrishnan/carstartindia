<h2> Honda Accord Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The Honda Accord has been around   for more than 15 years now and it has grown bigger, roomier and   aggressive over the years. While a whole lot of us still swear by the   Accord when it comes to a refined luxury experience this side of the   German saloons, the car has lost out in the diesel frenzy. Honda has   confirmed that it will bring the latest generation of the Accord at the   Auto Expo for a showcase.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd3.aeplcdn.com/600x337/cw/ec/21613/Honda-Accord-Right-Front-Three-Quarter-64768.jpg?wm=0" title="Honda Accord" alt="Honda Accord" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Honda is going to get the ninth generation of the Accord that is sold   in North America to India. The new Accord has not changed radically in   terms of looks. The car looks like an updated version of the current car   with a wider grille, headlamps with integrated LED daytime running   lamps and an aggressive bumper. While the side profile stays the same   (albeit a little bit shorter), it gets brand new LED tail lights.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd4.aeplcdn.com/600x337/cw/ec/21613/Honda-Accord-Right-Rear-Three-Quarter-64769.jpg?wm=0" title="Honda Accord" alt="Honda Accord" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The interior of the Accord has always been plush and stately and the   new Accord carries the legacy forward with modern touches. The car will   get a touch-screen infotainment system, keyless entry, automatic   headlamps and a rear-view camera with parking sensors. For comfort, you   have dual-zone climate control with rear AC vents, power seats for the   front row, leather upholstery and probably a sunroof too (the top-spec   Honda City gets one).</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd2.aeplcdn.com/600x337/cw/ec/21613/Honda-Accord-Interior-64770.jpg?wm=0" title="Honda Accord" alt="Honda Accord" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and Transmission</strong></span></p>
  <p><span id="spnLargeDescSynopsis">While the standard 2.4-litre i-Vtec four-cylinder petrol engine may   continue performing its duty under the hood, the Accord will certainly   get the hybrid setup. A 2.0-litre i-MMD four-pot petrol engine works in   consonance with an electric motor and a lithium-ion battery pack to   deliver over 190bhp power and 306Nm of torque. While the 2.4-litre   engine may be available with manual and automatic options, the hybrid   will get a CVT to transmit power to the front wheels.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd4.aeplcdn.com/600x337/cw/ec/21613/Honda-Accord-Interior-64771.jpg?wm=0" title="Honda Accord" alt="Honda Accord" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Price and Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Honda Accord will fight it out with its age-old competitor, the   Toyota Camry along with Volkswagen Passat and Skoda Superb. Expected to   be priced about Rs 25 lakh, Honda will certainly be looking forward to   get the hybrid car benefits and the rising sales of the Toyota Camry   Hybrid.</span></p>
