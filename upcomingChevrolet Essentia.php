<h2> Chevrolet Essentia Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Chevrolet has had quite a few   dull years in India except when they launched the Beat hatchback more   than half a decade ago. With the Trailblazer launched last year and the   imminent Cruze facelift, Chevrolet is trying for another comeback. The   Beat Cross will be launched this year, followed by the Beat-based   compact sedan next year along with the Spin MUV.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd3.aeplcdn.com/0x0/cw/ec/21789/Chevrolet-Beat-Front-view-65589.jpg?wm=1" title="Chevrolet Essentia " alt="Chevrolet Essentia " width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The compact sedan will be based on the Beat hatchback. The smiley   front fascia of a mesh grille with a bowtie splashed across and the   swept back headlamps will certainly be retained. While the side profile   should also be the same till the C-pillar, we would be very interested   to see the replacement of that very beautiful tail design. We hope it   does not turn out to be a cut paste job.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The insides should continue being futuristic with the blue backlit   instrument cluster with a digital speedometer and tachometer. While   leather upholstery would be a tough ask for this budget car, cabin space   would probably be better if the rear seats are pushed back owing to the   boot.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd1.aeplcdn.com/0x0/cw/ec/21789/Chevrolet-Beat-Rear-view-65591.jpg?wm=1" title="Chevrolet Essentia " alt="Chevrolet Essentia " width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Under the hood, you may get the same 936cc three cylinder   turbocharged diesel engine developing 57bhp of power. The 1.2-litre   petrol engine will be retained and both the engines will power the front   wheels through a five speed manual gearbox.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Price and Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The compact sedan will ideally compete against the Tata Sway, the   Zica based compact sedan rather than the Zest, Dzire and their likes.   The Chevrolet compact sedan is expected to be priced under Rs 5 lakh for   the petrol while the diesel may go up to Rs 5.5 lakh for the base   variants.</span></p>
