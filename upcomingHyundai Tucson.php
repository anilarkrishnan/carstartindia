<h2>Hyundai Tucson Preview </h2>
<div> <span id="spnLargeDescSynopsis">
  <p>Most of us would probably not   remember the butch Hyundai Tucson, the compact SUV that was launched in   India in April 2005. For those who remember, the Tucson was quite a   package back then â€“ a comfortable five-seater, good ground clearance, a   punchy diesel engine and a shift-on-fly four-wheel-drive. The Tucson is   set to make a comeback to India in its latest avatar and will be   showcased at the Auto Expo to be held in Greater Noida next month.</p>
  <img src="http://imgd1.aeplcdn.com/642x361/cw/ec/21795/Hyundai-Tucson-Exterior-65603.jpg?wm=1" title="Hyundai Tucson" alt="Hyundai Tucson" width="622" />
  <p><strong>Exterior</strong></p>
  <p>The Hyundai Tucson that is going to be showcased is a third   generation Tucson. The styling is modern and resembles the Santa Fe. The   gaping hexagonal front grille is flanked by swept back headlamps that   have two projector units apart from the reflector unit for high-beam.   The LED day-time running lamps are integrated on the fog-lamp housing on   the front bumper which also gets a faux skid plate. The side profile   also imitates the Santa Fe with the shoulder line climbing up towards   the tailgate. The black cladding around the perimeter of the car gives   it a rugged look.</p>
  <img src="http://imgd4.aeplcdn.com/600x337/cw/ec/21795/Hyundai-Tucson-Exterior-65604.jpg?wm=1" title="Hyundai Tucson" alt="Hyundai Tucson" width="622" />
  <p><strong>Interior</strong></p>
  <p>The interiors are not as dramatic as the exterior. The Tucson is a   five-seater SUV and the cabin looks spacious. The seats will get leather   upholstery as an option. The top spec variant may also get an   electrically adjustable driver seat. Being a Hyundai, the feature list   will be long. It will get climate control, an infotainment system with   touch-screen, steering-mounted audio controls along with other features   like push-button start, electrically operated mirrors and auto-dimming   internal rear-view mirror. Parking sensors with rear-view-camera may be   standard for the SUV.</p>
  <p><strong>Engine and Transmission</strong></p>
  <p>The Tucson may get the 1.6-litre inline four-cylinder diesel engine   with the 128bhp setup like the Elantra mated to a six-speed manual   gearbox. It also may get the 2.2-litre diesel mill from the Santa-Fe   with an automatic transmission and all-wheel-drive. There are also   chances of the introduction of it getting the new 2.0-litre CRDi diesel   engine available globally instead of the 2.2-litre mill. While petrol   engines are not really popular in this segment, looking at the Delhi   odd-even incident, Hyundai may also think of getting the 1.6-litre turbo   petrol or the 2.0-litre GDi engine which is available in the Sonata.</p>
  <p><strong>Price and Competition</strong></p>
  <p>The Tucson will be slotted between the Hyundai Creta and the Hyundai   Santa-Fe SUVs. The Tucson will be the ideal competitor for the Honda   CR-V and is expected to be priced between Rs 18 to 24 lakh.</p>
