      <h1><strong>How To Keep Your Car's AC Up And Running Smoothly?</strong></h1>
      <p><strong><strong>Best Ways To Maintain Your Car’s Air Conditioning</strong></strong></p>
      <p>In order to avail all the benefits of your air-conditioned car, you should take good care of your AC. Some of the most important elements included in an AC are evaporator, compressor, hoses and belts. These parts if not taken proper care can be damaged easily. In order to prevent unnecessary hassles, one should follow the below mentioned guidelines.</p>
      <p><img src="conditio.jpg" width="588" height="343" /></p>
      <p><strong>Tips To Keep Your Car’s AC in Proper Working Condition</strong></p>
      <p>⚫ Before the arrival of summer, it is advisable to consult a qualified technician for inspecting your car. The drive belts of the compressor gets damaged easily hence it’s advisable to check the compressor before the summer.</p>
      <p>⚫ In order to keep the air conditioning of your car in a proper condition, you should switch on your AC in winter for few minutes.</p>
      <p>⚫ Sometimes due to lack of refrigerant fluid, the air conditioning gets affected. It’s advisable to check the level of fluid and get it refilled when needed.</p>
      <p>⚫ Maintenance of the coolant pressure is necessary, which is why car owners should regularly use air conditioning while driving, at least for few minutes. Running your AC regularly also helps preventing hoses hardening and seals failure.</p>
      <p>⚫ Consulting a technician, when you feel the air conditioning is not working properly is advisable.</p>
      <p>⚫ You can prevent the dirty wet smell of the AC by setting the air-conditioning level at the lowest possible temperature with highest fan speed. Also put the de-frost mode on for 10 minutes. This process can easily eliminate the dirty smell from your car.</p>
