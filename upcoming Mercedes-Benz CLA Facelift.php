<h2> Mercedes-Benz CLA Facelift Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The Mercedes CLA had made its   debut in India as the CLA 45 AMG – a compact performance sedan in   mid-2014. The regular variants were launched early last year and have   been steady sellers for the German manufacturer. The CLA has recently   been updated globally and we expect the facelift to hit the Indian   shores in a short while just like the Mercedes A-Class facelift.</span></p>
  <p><span id="spnLargeDescSynopsis">The diamond grille in black is now standard across the trims and the   mesh design on the air dam have been reworked for a bit more aggression.   This is a midlife update for the CLA and the changes are not much. The   bumpers get a tweak and so do the internals of the headlamps. The   Mercedes CLA continues to get its evocative lines that blend in with the   Mercedes family face. The multi-spoke alloys are the only change to the   side profile.</span></p>
  <p><span id="spnLargeDescSynopsis">In terms of luxury, the premium sedan continues to have and   impressive list that includes leather upholstery, power adjustable   seats, sunroof and adjustable steering. On the inside, the display for   the infotainment system has been tweaked in terms of its appearance and   it also gets latest software like Android Auto and Apple Car Play for   seamless integration of your smart phones.</span></p>
  <p><span id="spnLargeDescSynopsis">The CLA will continue to be available in two guises – the CLA 200   petrol and the CLA200d diesel. The 2.2-litre diesel engine continues to   perform its duties and produces 136bhp of power and 300Nm of torque. The   1.6-litre four cylinder petrol mill produces 156bhp of power and 200Nm   of torque. Both the engines are mated to a seven-speed automatic   transmission with steering mounted paddle shifters and power the front   wheels of the sedan. It also gets driving modes in which the engine and   transmission characteristics change to optimise power delivery instead   of the standard economy function.</span></p>
  <p><span id="spnLargeDescSynopsis">The Mercedes CLA competes with the Audi A3 sedan in the compact   luxury sedan segment and the facelift is expected to be priced   marginally higher than the current car owing to the updates.</span></p>
</div>
