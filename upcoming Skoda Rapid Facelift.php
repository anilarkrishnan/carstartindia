<h2> Skoda Rapid Facelift Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">It has been more than four years   since the launch of the Rapid and Skoda is readying the facelift of the   C-segment sedan for a 2016 launch. This will be the first significant   facelift for the sedan that has been regularly tweaked and updated by   the Czech manufacturer. The Skoda Rapid will now resemble its larger   siblings â€“ the Octavia and Superb.</span></p>
  <p><span id="spnLargeDescSynopsis">The front fascia has become sharper with a restyled grille and the   almost rectangular headlamps. The front bumper and the fenders have been   reworked for the edgy look. The tail lamps have been tweaked as well   and the rear bumper sculpted to complete the new look. The sharp edges   gives the Rapid a sleek and premium look moving away from the   Fabia-esque look of the current car.</span></p>
  <p><span id="spnLargeDescSynopsis">On the inside, we do not expect many changes. While the dash will be   tweaked to go with the edgy exterior, it will get the Climatronic   climate control system with dust and pollen filters along with AC vents   for the rear passenger. The Skoda Rapid facelift might get new features   like a new Mirror Link touchscreen infotainment system with a reverse   camera option, cruise control and rain sensing wipers. In terms of   safety, the new Rapid will offer standard airbags and ABS with EBD. The   facelift model might also get electronically adjustable and foldable   ORVMs.</span></p>
  <p><span id="spnLargeDescSynopsis">Under the hood, you will have two engine options with both manual and   automatic transmissions. It will continue to get the 1.6-litre four   cylinder naturally aspirated petrol engine that develops 104bhp of power   and 153Nm of torque. The 1.5-litre turbo-diesel which also develops an   identical 104bhp of power but 250Nm of torque will also continue to be   offered. Both engines are mated to a five-speed manual gearbox. In case   of automatic transmissions, the petrol comes mated with a six-speed DSG   while the diesel is mated to a seven-speed DSG unit.</span></p>
  <p><span id="spnLargeDescSynopsis">The Skoda Rapid has been pitched against the Maruti Suzuki Ciaz, the   Honda City and the Hyundai Verna along with its twin, the Volkswagen   Vento. Also in the fray are the Renault Scala and the Nissan Sunny. The   facelifted Rapid is expected to be priced competitively and marginally   above the current car.</span></p>
