<h2> Chevrolet Spin Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The last two years have seen a   sudden surge in demand for MPVs in the Indian market. All the major   players have at least one vehicle in this body style and by the look of   things, seem to be doing quite well with decent volumes to show as   proof. This has presented itself as a case for manufacturers to expand   their presence by introducing vehicles at different price ranges and it   seems the first one to take up this challenge will be Chevrolet with its   Spin MPV.</span></p>
  <p><span id="spnLargeDescSynopsis">It has been selling the Enjoy since mid-2013 in a lower price bracket   while its older guard, the Tavera, has been relegated to fleet market.   The Spin will take the place of the Tavera in the upper section of the   MPV segment.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Chevrolet Spin gets a mixture of looks from all the Chevrolet   models. The fascia looks to be similar to that of the Beat/Spark as well   as the Trax compact SUV. It gets the pronounced hood as well as the   large golden bowtie logo that is biased to the top half of the mesh   grille.  The front windshield is sharply raked and flows from the bonnet   to the rails on the roof. There is a chrome outline around the grille   as well as the fog lamp housing.</span></p>
  <p><span id="spnLargeDescSynopsis">The side profile reveals typical MPV lines with the flat roof, wide   doors and extended C-pillar which hides the third row. The rear has a   square(ish) design with flat elements save for a ridgeline into which   the Chevy logo and locking mechanism have been placed in the middle.   There is black cladding around the outer edge of the rear glass to give   the appearance of a floating rear roofline.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Chevrolet Spinâ€™s interior has all the standard Chevy elements.   The cabin has been kitted out in light brown plastics for markets like   Thailand and Indonesia. However, we in India will get a beige trim with   black plastics. Standard features on the top-spec variant are expected   to include climate control, touchscreen infotainment system, rear   parking sensors with camera, electrically adjustable ORVMs and power   windows for the first and second rows. Also expected are a plethora of   cubby holes and storage spaces across all three rows.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Globally the petrol engine for the Spin is a 1.5-litre four-cylinder   unit that produces 107bhp and 148Nm of torque and is offered with a   six-speed AT. We in India will get this same output from the 1.4-litre   petrol engine with power going to the front wheels via a five-speed   manual.</span></p>
  <p><span id="spnLargeDescSynopsis">The diesel, which is expected to be their major seller, will get a   reduced version of the 2.0-litre unit from the Cruze, most probably in   the capacity of 1500cc. It is expected to have an output of around   110bhp and 340Nm of torque and will be offered with a six-speed manual.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Price and competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">It is expected to be priced in the range of Rs 10 lakh to Rs 15 lakh   and will be offered in both seven and eight seat configurations with the   latter only for the lower spec models. When the Chevrolet Spin is   launched it will compete with the Toyota Innova, Nissan Evalia and the   Renault Lodgy.</span></p>
