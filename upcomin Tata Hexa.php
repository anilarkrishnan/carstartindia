<h2> Tata Hexa Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Since the launch of the Zest   compact sedan, Tata has not looked back at its old rusty and crude self.   The Hexa will certainly benefit with the renewed focus on quality and   the new design language – Horizon Next. The latest SUV from Tata is   based on the Aria and lends a fresh lease of life to the otherwise not   so popular people mover.</span></p>
  <p><span id="spnLargeDescSynopsis">The Hexa is a new SUV measuring 4,764mm in length, 1,895mm in length   and 1,780mm in height. The vehicle has a long wheelbase of 2,850mm. The   car's fascia boasts of newly designed projector headlamps, a honeycomb   grille and new bumper with fog lamps. The horizontal chrome slat under   the grille extends towards the headlamps and the honeycomb pattern can   also be seen on the front chin.</span></p>
  <p><span id="spnLargeDescSynopsis">Coming to the sides, the newly designed 19-inch alloy wheels can be   seen. Though the overall design is similar to the Aria, the Hexa gets a   sporty design language which is continued at the rear as well. The rear   bumper, with dual exhaust tips, gets a dual-tone paint scheme with a new   shape for the reflectors. The tail lamps are restyled and come with LED   units. A new spoiler can also be seen at the back.</span></p>
  <p><span id="spnLargeDescSynopsis">Inside the car, Tata has gone all black with silver coloured accents   to give the interior a premium feel. It gets automatic climate control   with a separate zone AC for the rear and third row passengers. The cabin   is roomy and looks like the third row should be able to allow full-size   adults to sit comfortably. The top-spec car will get leather   upholstery, steering mounted controls for the infotainment system,   electrically adjustable and retractable outside rear view mirrors. The   audio system gets a display screen and seems to be sourced from the   Zest.</span></p>
  <p><span id="spnLargeDescSynopsis">On the safety front, it will get two airbags at least along with   parking sensors and possibly a rear view camera with guidance. Driver   aids like ABS with EBD and ESP will be available. The Hexa also gets   driving modes where you can choose between Comfort, Dynamic and Rough   Road modes or just leave it in Auto for it to choose the optimum option.</span></p>
  <p><span id="spnLargeDescSynopsis">Under the hood, you will have the Varicor 400 – a 2.2-litre   turbocharged four cylinder diesel engine – that develops 154bhp of power   and 400Nm of torque. The Hexa may well turn out to be the most powerful   SUV in its segment and will get a six-speed manual transmission. The   Hexa may also get the all-wheel-drive option like the Aria.</span></p>
  <p><span id="spnLargeDescSynopsis">While we do not have any details on the variant list and their   equipment levels, we know for a fact that the Hexa has been built to   compete with the Toyota Innova and the Mahindra XUV500. With the Innova   Crysta coming into picture, the dynamics of the segment have taken an   interesting turn. We expect Tata to price the Hexa competitively between   Rs 13 lakh to Rs 18 lakh.</span></p>
