    <td><h1 itemprop="name headline">Audi India Offers Discount upto ₹8.5 Lakh On Selected Models</h1>
    <p align="center"><img src="ofaudi1.jpg" width="660" height="301" /></p>
    <p class="style1">German Luxury car maker Audi India has announced a discount of up to ₹8.5 lakh on purchase of its vehicles under the 'Spring Collection'.</p>
    <p class="style1">The cars under the Audi's new 'Spring Collection' include A4, A6 sedans and Q5, Q7 Sports utility vehicles (SUVs).The offer will be available in limited numbers, said Audi India. As per the advertisement on the company website, the Audi A4, which was originally priced at a starting range of ₹32.81 lakh will be now available in the domestic market with a discount of ₹4.50 lakh. The second vehicle, which is on discount is the A6 .The car will be now retailed in the market with a starting price of ₹36.07 lakh as against the ₹43.31 lakh earlier.</p>    
    <p class="style1">Earlier last week, Audi India has launched Q3 S Edition at ₹24.37 lakh (ex-showroom, Delhi), which is about ₹62,000 cheaper than earlier. The Q3 S, the cheapest offering of Audi in the domestic market has been getting tremendous response from the auto lovers. The company is also offering 'Zero-down payment' booking and EMI of ₹27,700 for the cars.</p>
    <p class="style1">Audi Q3 S comes with 2.0-litre diesel motor, which can churn out a power of 140bhp and offers a fuel efficiency of 17.71kmpl.The car offers features like rear parking sensors, cruise control, driver information system, six airbags, electronic stabilisation system and electromechanical parking brake.</p>
    <p class="style1">Meanwhile, 2014 Audi A8L has been spied testing in India, hinting at the imminent launch of vehicle in the sub-continent market. The car was spotted with Matrix LED headlights, which is first of its kind in any vehicles.</p>
