<h2> Chevrolet Beat Facelift Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Chevrolet had introduced their   compact hatchback, the Beat, in India back in 2011. Since then, the car   has been more or less the same but for a few nips and tucks. For its   latest update, the new Beat will be decked up with revised cosmetics to   give the compact hatchback a fresh lease of life.</span></p>
  <p><span id="spnLargeDescSynopsis">The new Beat will continue to be built on the same platform and the   facelift is expected to get quite a few changes. Aesthetically, the new   Beat will draw inspiration from the Chevrolet Essentia. It will retain   the smiley face with the hexagonal grille and big swept back headlamps   though a bit more chiselled.</span></p>
  <p><span id="spnLargeDescSynopsis">The interior will continue with the same layout, with black and grey   hues for the for the futuristic-looking digital instrument cluster. With   the platform remaining the same, the improvements in cabin space will   be restricted but the learnings from carving out more space for the   Essentia may have an effect. In terms of equipment, it will be limited   to being a budget car.</span></p>
  <p><span id="spnLargeDescSynopsis">Mechanically, the Beat will more or less be the same with the   1.0-litre three-cylinder turbocharged diesel and the 1.2-litre four   cylinder petrol engine options. Both the engines will power the front   wheels through a five-speed manual gearbox. With added impetus to   safety, the top-spec Beat may get ABS-EBD and dual front airbags.</span></p>
  <p><span id="spnLargeDescSynopsis">The Chevrolet Beat will continue to compete with the likes of the   Maruti Suzuki Celerio, Wagon R, Renault Kwid, Hyundai i10 and the Tata   Tiago. It will be launched in India in April.</span></p>
