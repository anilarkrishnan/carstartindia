<h2> Audi A5 Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Audi has unveiled the brand new   A5 Coupe and its performance version the S5 Coupe internationally. Now   if you are thinking what is the India relevance of this car – we have   spotted the yet to be globally unveiled four-door Sportback testing on   Indian roads. Which makes us think the A5 Sportback might not be far   from its launch in the country.</span></p>
  <p><span id="spnLargeDescSynopsis">If not evident yet, the A5 has two body shells – the two-door Coupe   and the four-door Sportback. And then depending upon the performance it   packs we have the regular A5, the madder S5 and the insane RS5. While   the S5 is available in both body shells, the last RS5 was a two-door   Coupe.</span></p>
  <p><span id="spnLargeDescSynopsis">The new generation A5, like other next-gen Audis, looks almost the   same as its predecessor. But looks apart, there is almost nothing that   gets carried over in the new generation car. In case of the Coupes, the   A5 is now longer, thanks to the longer wheelbase, and wider. Inherently,   the cabin has become more spacious and the boot has grown by 10 litres   to 465 litres. Owing to the multi-material approach in building the car,   Audi has saved about 60 kg weight in the new Coupes.</span></p>
  <p><span id="spnLargeDescSynopsis">Along with a new set of engines – three diesel and two petrol   engines, the A5 now features virtual cockpit and the MMI Touch along   with the standard creature comforts of luxury sedans. The engines   develop power between 185bhp and 280bhp with over 15 per cent better   performance and 20 per cent better fuel economy. The S5 is powered by   the 345bhp forced induction 3.0-litre V6 petrol engine and drives all   four wheels.</span></p>
