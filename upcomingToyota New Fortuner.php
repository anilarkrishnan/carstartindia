<h2> Toyota New Fortuner Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The current Fortuner has been on   sale in the Indian market since 2009 and thanks to its muscular and   rugged looks, has a good fan following. Toyota has now officially   revealed their most anticipated product of the year – the 2016 Fortuner.   Like the current generation Fortuner, the 2016 version of SUV is also   based on the Hilux platform and boasts of a new design and big list of   features.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The next-gen variant of the Fortuner  gets a complete design overhaul   and looks sharper than the present model. The fascia sports a new   two-slat grille that extends up to the headlamps and has flanks that   taper over the headlights. Toyota has equipped the Fortuner with LED   daytime-running lamps and the top-end model will also get Bi-LED   projector headlamps. These sweptback projector headlamps are surrounded   by chrome embellishments to add to the bling factor.</span></p>
  <p><span id="spnLargeDescSynopsis">The bumper is redesigned and gets a new black air dam while the fog   lamp housings are surrounded by thick curved aluminium accents. On the   side, Toyota has given the new Fortuner a floating pillar-less roof   design that makes the SUV look quite interesting and attractive. The   rear end also gets a similar slim-cut treatment with sleek tail lamps.   The vehicle will be available with 17-inch and 18-inch wheel options.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">And it’s not just the exterior, but Toyota has completely redesigned   the cabin of the Fortuner as well. The dashboard layout is new and the   instrument cluster comes with a multi-information display. The center   console is vertically-oriented with a touch screen entertainment system,   automatic AC controls, and a lot of storage spaces. The new three-spoke   leather-wrapped steering wheel comes with audio controls and the car   gets a new key fob. Higher variants are likely to come with rear parking   sensors, keyless smart entry and start, downhill assist control, a   powered tailgate and 18-inch alloys with highway tyres.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">One of the major changes on the next-gen Fortuner will be Toyota’s   new GD series engines. The new 2.4-litre turbo-diesel is expected to   replace the current 2.5-litre diesel unit, and a 2.8-litre mill will   replace the current 3.0-litre unit. The new 2.4-litre 2GD-FTV motor is   rated at 148bhp of power and 400Nm of peak torque. The 2.8-litre 1GD-FTV   engine churns out 175bhp and 450Nm of torque. Depending on the variant,   the transmission includes a six-speed manual or a six-speed automatic.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Price and Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">Toyota Kirloskar Motors will bring the car to the 2016 Auto Expo in   India and might also launch it for the Indian market. It is expected to   be priced in the Rs 25 lakh to Rs 28 lakh price bracket and will then   lock horns with the Ford Endeavour, Mitsubishi Pajero Sport and the   upcoming Chevrolet Trailblazer.</span></p>
