<h2> Toyota Hiace Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Toyota is currently the most   successful manufacturer of MPVs  in India thanks to the Innova and   previously the Qualis. Both vehicles took their respective segments by   storm with the former still clocking the highest figures in the segment a   full 10 years after it was launched. Now it appears that Toyota wants   to raise the stakes by introducing a vehicle larger than the Innova .   This MPV is called the Hiace and is expected to make an Indian debut by   the middle of this year.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The India- spec Hiace will be the super long wheelbase commuter bus   version which is 5.3-metres in length and has a wheelbase of 3.1-metres.    It does not look spectacular and is a vehicle where purpose takes a   much higher importance than looks. The facia is a combination of chrome   and many horizontal lines to imply width and space within. The   windscreen is large while the roof is high and reminiscent of what we in   India saw in the original Maruti Suzuki Omni which came out in 1980s.</span></p>
  <p><span id="spnLargeDescSynopsis">The Hiace gets a single sliding door on the left or right depending   on RHD/LHD which obviously for the Indian market will be former. It gets   15-inch alloy wheels and lightly flared wheel arched to  give the Hiace   a squat stance.</span></p>
  <p><span id="spnLargeDescSynopsis">Toyota has made the rear a little unspectacular but this seems to go   well with the overall image of the MPV. The only chrome bits are the   logo, badges and a small strip above the number plate slot. The entire   hatch door opens up and there is ample room to load cargo into the van.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">It has been designed to seat 10 people with a combination of two   seats on the right and one on the left. The fabric is a combination of   grey and black while all the plastics are white in colour. The feature   list is very similar to that of the Innova but we believe that as a   dealer option, Toyota will offer a screen as well as a partition    between the passenger rows and the driver section.</span></p>
  <p><span id="spnLargeDescSynopsis">The boot space is massive with what looks to be at least 3000-litres   available for use behind the last row. However, it is possible that   Toyota may, at the dealer level, offer individual shelves or jump seats   in row four to increase practicality with little extra cost.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The India-spec  Hiace will get a 3.0-litre turbocharged diesel engine   that produces 134bhp@3400RPM and 300Nm of torque in the range of   1200rpm to 2400RPM. This kind of torque available at such a low RPM   means the vehicle has a lot of low end power  and will need little   effort to move in heavy traffic.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Price and target market</strong></span></p>
  <p><span id="spnLargeDescSynopsis">We expect that Toyota will price the Hiace in the range of Rs 22 lakh   to Rs 26 lakh and has aimed it at schools, fleet operators and hotels.   If the Hiace is successful, then it will open the market for vehicles   like the VW Caravelle and higher up the price range the Mercedes-Benz   Viano.</span></p>
