<h2> Toyota Etios Facelift Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Toyota will launch the new 2016   version of the Etios sedan on September 13. The second upgrade of the   budget sedan will include changes to the exterior as well as the   interior along with additions to the feature list.</span></p>
  <p><span id="spnLargeDescSynopsis">The new 2016 Toyota Etios is a facelift and will carry the same body   shell as the existing car. The styling has undergone quite a few changes   with a new front fascia. The blacked out air dam has been split into   two with and houses the fog lamps. The rear bumper gets additional   contours to make it look premium.</span></p>
  <p><span id="spnLargeDescSynopsis">A majority of changes will be seen inside the new Etios with the   interiors being revamped to feel premium. The dashboard layout remains   the same but the centrally mounted instrument cluster will get a digital   treatment. The 2-DIN music system will now offer a touchscreen   displayalong with electrically operated wing mirrors and steering   mounted audio controls.</span></p>
  <p><span id="spnLargeDescSynopsis">With a renewed focus on safety, the sedan might get dual front   airbags and ABS standard across all variants. Engine performance and   driving feel will remain unchanged but you can expect better noise   insulation in the new Etios. Under the skin, the Etios will continue   with its reliable 1.5-litre petrol engine option and the 1.4-litre   diesel mill.</span></p>
  <p><span id="spnLargeDescSynopsis">The new Toyota Etios will have to fend competition from the compact   sedans with the equipment they offer for a lower price. Low maintenance   costs, reliability and cabin space along with larger boot-size, though,   will work in favour of the Etios. To be launched next month, we expect   the new Toyota Etios to be priced aggressively with an introductory   price tag of under Rs 7 lakh.</span></p>
