    <td><h1 itemprop="name headline">Ferrari launches swanky 488 GTB in India at Rs 3.88 Crore</h1>
    <p align="center"><img src="offerrari.jpg" width="642" height="369" /></p>
    <p class="style5">Italian supercar maker Ferrari announced the launch of the new Ferrari 488 GTB in India on Monday Feb. 17. The sports car has been priced at Rs 3.88 crore (ex-showroom, New Delhi).</p>
    <p class="style5">Ferrari, which officially entered into the Indian markets in last August with the launch of California, will sell the new 488 GTB through the company dealerships located in New Delhi and Mumbai. Ferrari 488 GTB is the successor of the 458. The 488 moniker points to each cylinder's cubic capacity, 488cc (3902cc divided by 8 cylinders). The GTB, which stands for Gran Turismo Berlinetta, pays homage to the several iconic models from Ferrari.</p>    
    <p class="style5">The 488 GTB is powered by a 3.9-litre twin-turbo V8 engine that develops 661bhp at 8,000rpm and 760Nm of torque at 3,000rpm. The engine is mated to automated manual dual-clutch, seven-speed F1 gearbox. The 488 GTB is able to achieve 0-100kmph in just 3.0 seconds, before attaining a top speed of over 330kmph.From a visual point, although the Ferrari 488 GTB shares similarities with its predecessor, the 458, the sports car gets revised front fascia with new headlamps and bonnet. Designed by the Ferrari Styling Centre, Ferrari 488 GTB measures 4568mm in length, 1952mm in width and 1213mm in height.</p>
