<h2> Datsun GO-Cross Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">The crossover and SUV market in   the Indian subcontinent is at its lucrative best for both the   manufacturers as well as the buyers. As a buyer, you get something that   resembles an SUV for the price of hatch. The latest entrant to the mass   market of passenger vehicles in India â€“ Datsun, has its eyes set on this   segment. After the launch of the affordable Datsun Go, Redigo and the   super compact MPV Go+, the Nissan sub-brand will get the Go-Cross to   India. Unveiled globally at the Tokyo Motor Show in 2015, the Go-Cross   was seen in India for the first time at the Auto Expo 2016.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Datsun Go-Cross is based on the Go+ MPV and so has almost the   same proportions. What is new, however, is the contrast coloured body   kit which for this concept car is grey around the sides and silver on   the pseudo bash plate elements.</span></p>
  <p><span id="spnLargeDescSynopsis">Datsun has fitted this concept car with projector headlamps and LED   DRLs which will become standard headlights when the vehicle goes into   production. One of the other noticeable elements on the car is the black   roof tray with rails which, we believe, will be carried over to the   production model. There is a small rear spoiler and fancy looking   C-shaped tail lamps which will likely be carried over into production.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">There are currently no pictures of the interior but going by how   Datsun has kitted out the cabin of its other models, we can safely   assume that this vehicle too may get an amalgamation of the standard   elements. AC, AUX-IN interface and space for about five people   (realistically) are expected to be on the offer list.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">On the engine front, we expect the 1.2-litre three-cylinder petrol to   be the standard offering. There is a high possibility that this may be   the first model in the Datsun line-up to be offered with a diesel   engine, which, in this case, will be the 1.5-litre dCi that powers a   variety of Nissan and Renault models. A five-speed manual will be common   across all variants.</span></p>
  <p><span id="spnLargeDescSynopsis"><strong>Price</strong></span></p>
  <p><span id="spnLargeDescSynopsis">We may probably get to see a near production version of the Go-Cross   and expect it not to be too far away from the concept. Being a concept   car, it will be hard to predict any price. However, we believe that this   vehicle will slot in above the Go+ and have a starting price in the   range of Rs 4.5 lakh. It is expected to be launched sometime next year.</span></p>
