<h2> Tata Kite 5 Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Tata Motors had for a long time   been testing replacements for the Indica and the Indigo eCS. It took the   wraps off the Indica replacement last year and dubbed it the Zica. It   is now expected to get a replacement for the Indigo eCS which it is   likely to call the Sway. The revamped vehicle is expected to offer good   value for money for consumers on a budget. Tata will showcase the car at   the 2016 Auto Expo.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd1.aeplcdn.com/0x0/cw/ec/21916/Tata-Sway-Left-Side-View-66216.jpg?wm=1" title="Tata Kite 5" alt="Tata Kite 5" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Exterior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">We have seen the Tata Zica in the metal and know that the Sway will   look just the same all the way to the C-pillar. However, in a bid to   separate the two cars Tata is likely to modify the grille of the latter.   The side profile will mimic that of the Zica while the boot (based on   the test mules) will be stubby and have the same tail lamps as the   hatchback. However, it is also likely to get some additional chrome and   will get the number plate on the bootlid.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd4.aeplcdn.com/0x0/cw/ec/21916/Tata-Sway-Rear-view-66217.jpg?wm=1" title="Tata Kite 5" alt="Tata Kite 5" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Interior</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The Tata Sway will get the same swanky cabin as the Zica right down   to the layout of the instrument cluster. Design elements include a grey   and black dashboard with grey seats, colour inserts for the aircon   vents, 2-DIN music system, climate control and steering mounted audio   controls. However, given that this is a compact sedan, the space at the   back will definitely be more than that of the hatchback. Expect boot   space to be in the region of 350-litres to 400-litres.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd1.aeplcdn.com/0x0/cw/ec/21916/Tata-Sway-Rear-view-66219.jpg?wm=1" title="Tata Kite 5" alt="Tata Kite 5" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Engine and Gearbox</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The petrol engine for the Sway will be the 1.2-litre Revotron motor   that produces 84bhp/140Nm of torque. The diesel on the other hand, will   be the 1.0-litre turbo three-cylinder unit that produces 69bhp/140Nm of   torque which by the look of things may seem inadequate. Both engines are   offered only with a five-speed manual.</span></p>
  <span id="spnLargeDescSynopsis"><img src="http://imgd1.aeplcdn.com/0x0/cw/ec/21916/Tata-Sway-Tail-lamps-66218.jpg?wm=1" title="Tata Kite 5" alt="Tata Kite 5" width="622" /> </span>
  <p><span id="spnLargeDescSynopsis"><strong>Price and Competition</strong></span></p>
  <p><span id="spnLargeDescSynopsis">The new Sway compact sedan will be placed below the Zest and might be   priced in the Rs 4 lakh to Rs 5 lakh range. At this price point for a   budget sedan segment, it will create a huge impact on the sub-four metre   sedans like the Honda Amaze, Maruti Suzuki Swift Dzire and the Hyundai   Xcent.</span></p>
