<h2> Ferrari GTC4 Lusso Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">It is a Ferrari and is   essentially a station-wagon. Knowing Ferrari’s legacy of building   superfast sportscars it may seem out of place. Meet the Ferrari GTC4   Lusso – the three door four-seater shooting brake from the Italian   stable. The GTC4 Lusso is the next generation Ferrari FF which was built   as a grand tourer to carve continents at breath taking speeds   comfortably.</span></p>
  <p><span id="spnLargeDescSynopsis">The front fascia is dominated by a wide single-piece horizontally   slatted air dam that extends across the width of the car with the   prancing horse emblem in the centre. The Ferraro 458 style headlamps are   swept back on the contoured hood with flared fenders on the side.   Moving on to the side, the Ferrari has the familiar low slung profile   with large alloy wheels filling up the wheel arches. The long hood is   complemented by the breathing gills on the fenders. The raked windshield   means a low slung roof that almost extends to the back of the car –   typical for shooting brakes. The tail is also as dramatic as the whole   car with the tapering cabin, wide rear bumper with rear splitter and the   quad exhausts.</span></p>
  <p><span id="spnLargeDescSynopsis">Wrapped in leather the function-focussed interior of the Ferrari GTC4   Lusso is exquisite especially with the carbon fibre inlays. Like all   grand tourers, it gets a touch-screen infotainment system, climate   control, power adjustable front seats et al. The three spoke steering   wheel gets the typical flurry of switches that can control everything   from driving modes, car setup and gear shifts along with Bluetooth   telephony. The instrument cluster has the rev counter taking centre   stage while speed and other vitals are displayed on the two TFT screens   around the tachometer. Even the co-driver gets a peek at exactly how   fast you are driving through a screen embedded in the dashboard   displaying rpm, speed and the gear you are driving in.</span></p>
  <p><span id="spnLargeDescSynopsis">The GTC4 Lusso is powered by the massive 6.3-litre Ferrari V12 that   produces 680bhp of power and 697Nm of torque. The naturally aspirated   mill powers the Ferrari past 100kmph in almost 3.5 seconds to a top   speed of 345kmph. It also gets an improved version of the dual-gearbox   four-wheel-drive setup from the FF along with four-wheel-steering as   well.</span></p>
  <p><span id="spnLargeDescSynopsis">For safety, you are cocooned between four airbags – two upfront and   two curtain airbags. It gets traction control, launch control, skid   control and a host of other electronic aids as a part of its electronic   stability program. As far as competition is concerned, there is the   Aston Martin Rapide and the Porsche Panamera but none of them comes   anywhere close to the Italian in terms of price as well as performance.</span></p>
