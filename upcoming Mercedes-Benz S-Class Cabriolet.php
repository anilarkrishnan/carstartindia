<h2> Mercedes-Benz S-Class Cabriolet Preview </h2>
<div>
  <p><span id="spnLargeDescSynopsis">Mercedes-Benz is the first of the   global luxury manufacturers to come to India. Indians have a soft   corner for the three-pointed stars and Mercedes has responded by   gradually getting its entire global line-up to India. Based on the   S-Class coupe, the S-Class Cabriolet is another exotic in the bouquet of   premium offerings.</span></p>
  <p><span id="spnLargeDescSynopsis">The front fascia is that of the S-Class but with a thick chrome slat   and the three-pointed star. The bumper gets aggressive creases to suit   the sporty character of the convertible. The long hood, raked   windshield, four seats and the stubby boot are a perfect recipe for the   side profile of a classic roadster.</span></p>
  <p><span id="spnLargeDescSynopsis">It is an S-Class and the flagship drop-top offered by Mercedes   globally. Luxury, hence, comes in abundance. The pure white Nappa   leather interiors laced with wooden inserts and top quality plastics   give the S-Cabriolet cabin a classy and plush look. We need not really   talk about the equipment and features as the top of the line convertible   gets everything from automatic intelligent LED headlamps with DRLs,   power seats with memory function, three-zone climate control et al. The   electrically operated roof opens or closes in 20 seconds and can be   operated up to speeds of 60kmph. The state of the art Burmester audio   system floats with the roof to offer the best of music experiences with   the roof closed or open.</span></p>
  <p><span id="spnLargeDescSynopsis">Under the hood, you have the 4.7-litre twin-turbocharged petrol V8   engine powering the rear wheels. The 449bhp of power and 700Nm of torque   are transferred through a nine-speed automatic gearbox. As for the   safety, you have the Mercedes safety web of dual-stage airbags and the   electronic stability program with a host of features including ABS-EBD   with brake assist, anti-skid control, traction control, hill-assist and   adaptive cruise control with emergency braking.</span></p>
  <p><span id="spnLargeDescSynopsis">After the showcase at the 2016 Auto Expo held in Greater Noida, the   S-Class cabriolet will be launched soon and should be priced upwards of   Rs 2 crore in India. The S-Cabriolet competes with the Bentley GTC, the   Maserati Gran Cabrio and the Ferrari California in terms of the expected   price tag.</span></p>
